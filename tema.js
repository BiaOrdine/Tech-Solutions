function artista1(){
    fetch('https://raw.githubusercontent.com/BiaOrdine/RAWs-techsolutions/refs/heads/main/tema.json').then(resposta => resposta.json()).then(artistas =>{
    
     document.getElementById('imagem').innerHTML = artistas.foto1;
     document.getElementById('nomeartista').innerHTML = artistas.artista1;
     document.getElementById('dataartista').innerHTML = artistas.data1;
     document.getElementById('texto').innerHTML = artistas.texto1;

     })
}

